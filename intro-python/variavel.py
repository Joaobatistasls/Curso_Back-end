# Tipo de dado Texto(Strings - Str)
nome = 'João'
print(type(nome))

# Tipo de dado númerico(Inteiros(int) e Decimais(float))
idade = 20
print(type(idade))

# Tipo de dado Booleanos(bool)
vc_e_homem = True
print(type(vc_e_homem))

# Tipo de dado Vazio(NoneType)
telefone_fixo = None
print(type(telefone_fixo))