# Conseguimos utilizar as 4 operações basicas
# + ( Soma )
# - ( Subtração )
# * ( Multiplicaçõa )
# / ( Divisão )
# // ( Divição Interia )
# ** ( Potenciação ou exponenciação )
# % ( Resto da Divisão )

numero_1 = 10
numero_2 = 5
print (numero_1 + numero_2)
print (numero_1 - numero_2)
print (numero_1 * numero_2)
print (numero_1 / numero_2)
print (numero_1 // numero_2)
print (numero_1 ** numero_2)
print (numero_1 % numero_2)

